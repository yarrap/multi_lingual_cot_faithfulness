# ── Math scaffold words (English prose left after LaTeX stripping) ──────────
MATH_SCAFFOLD = re.compile(
    r'\b(let|where|such\s+that|therefore|hence|thus|given|suppose|assume|'
    r'equals?|equal\s+to|times|divided\s+by|plus|minus|mod|modulo|'
    r'over|under|per|of|by|the|a|an|'
    r'substitute|solve\s+for|check\s+if|domain|range|interval|'
    r'probability|sequence|condition\s+for|calculate|determine|'
    r'initial|total|area|distance|point|option|term|terms|'
    r'then|so|and|or|as|thus|true|false)\b',
    re.IGNORECASE
)

def _is_variable_soup(text: str) -> bool:
    """
    Returns True if the text is predominantly single math variable letters
    with no real words (≥3 chars) surviving.
    E.g. 'x y x y', 'p q x p q x', 'k k k k k k' → True
         'সুতরাং x y' → False (has real non-ASCII word)
    """
    tokens = text.split()
    if not tokens:
        return True
    # Count tokens that are single Latin letters or short math combos (dx, dy, pq, etc.)
    math_tokens = sum(
        1 for t in tokens
        if re.fullmatch(r'd?[a-zA-Z]{1,2}[\'′]?', t)  # x, y, dx, dy, f', etc.
        or re.fullmatch(r'[!×÷±]', t)
    )
    real_word_tokens = sum(
        1 for t in tokens
        if len(t) >= 3 and re.search(r'[^\x00-\x7F]', t)  # non-ASCII (Bengali, Chinese, etc.)
        or (len(t) >= 4 and re.fullmatch(r'[a-zA-Z]+', t)   # longer ASCII words
            and not re.fullmatch(r'(mod|sin|cos|tan|log|exp|lim|sup|inf|'
                                  r'dx|dy|dz|dr|det|arg|max|min|lcm|gcd)', t, re.I))
    )
    # Soup if >70% math tokens and no real words
    return real_word_tokens == 0 and (math_tokens / len(tokens)) >= 0.7


def remove_maths(cot_trace: list) -> list[str]:
    clean_cot_trace = []
    for trace in cot_trace:

        # Block math ($$...$$) then inline ($...$), then \[...\] \(...\)
        trace = re.sub(r'\$\$.*?\$\$', ' ', trace, flags=re.DOTALL)
        trace = re.sub(r'\$.*?\$', ' ', trace, flags=re.DOTALL)
        trace = re.sub(r'\\\[.*?\\\]|\\\(.*?\\\)', ' ', trace, flags=re.DOTALL)

        # LaTeX commands + their braced arguments (\frac{a}{b} → gone)
        trace = re.sub(r'\\[a-zA-Z]+\s*(?:\{[^}]*\})*', ' ', trace)

        # Numbers
        trace = re.sub(r'\d+([,.\s]\d+)*', ' ', trace)

        # Units
        trace = re.sub(
            r'\b(px|cm|mm|km|kg|g|mg|ml|l|m|s|hz|ghz|mhz|mph|ft|lb|oz)\b',
            ' ', trace, flags=re.IGNORECASE
        )

        # Math symbols and structural characters
        trace = re.sub(
            r'[\\[\](){}*&^%$#@~=+|<>/_ΔΩδ√∑π∞∫≈≠≤≥±×÷→←↔∈∉⊂⊃∪∩′]',
            ' ', trace
        )

        # Standalone math function names (only when isolated, not part of real words)
        trace = re.sub(
            r'(?<!\w)(sin|cos|tan|log|exp|lim|sup|inf|det|mod|lcm|gcd|dx|dy|dz|dr)(?!\w)',
            ' ', trace, flags=re.IGNORECASE
        )

        # Multiple-choice labels: standalone A. B: C) etc.
        trace = re.sub(r'(?<!\w)[A-Z][.:](?!\w)', ' ', trace)

        # Spaced-out factorial/operator clusters: "! ! ! !" or "n! k!"
        trace = re.sub(r'(\b\w{0,3}[!]\s*){2,}', ' ', trace)

        # English math scaffold words
        trace = MATH_SCAFFOLD.sub(' ', trace)

        # Punctuation clusters
        trace = re.sub(r'([.,:;"\'`!?]\s*){2,}', ' ', trace)

        # Orphan punctuation
        trace = re.sub(r'\s+[,.、。:;]\s+', ' ', trace)

        # Normalize whitespace
        trace = re.sub(r'\s+', ' ', trace).strip()

        if not trace:
            continue
        if len(trace) <= 5:
            continue
        if re.fullmatch(r'[\W\s]+', trace):  # only non-word chars
            continue
        if _is_variable_soup(trace):          # pure math variable residue
            continue

        clean_cot_trace.append(trace)

    return clean_cot_trace

def compute_lcr(cot_trace: list, prompt_lang: str):
    """
    Compute the Language Compliance Rate (LCR) metrics for CoT multilingual evaluation

    Args:
    cot_trace: full CoT reasoning trace generated by model
    prompt_lang: expected language e.g 'sw' for Swahili

    Returns:
    lcr: float between 0 and 1
    """
    chunks = extract_reasoning_steps(cot_trace) # preprocess cot trace
    chunks = remove_maths(chunks)

    n = len(chunks)
    lcr = 0
    lang_code = LANG_TO_CODE[prompt_lang]

    if len(chunks) == 0:
        return 1      

    for trace in chunks:
        # Remove all newline characters from the trace before passing to predict
        cleaned_trace = trace.replace('\n', ' ').replace('\r', ' ').strip()
        
        if not cleaned_trace: # Skip empty strings after cleaning
            continue
        pred = model.predict(cleaned_trace, 3)
        if any(lang_code in pred for pred in pred[0]):
            lcr += 1
        else:
          print("="*10)
          print(prompt_lang, pred[0], "\n", trace)
          print("Full text: --->")
          print(cot_trace)
          print("="*10)
          lcr += int(input("Correct value between 0 and 1: ")) # manually inspect some traces

    return lcr / n

